function applyLanguage(lang) {
  const dict = translations[lang] || translations.uk;

  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.dataset.i18n;
    if (dict[key] !== undefined) el.textContent = dict[key];
  });

  localStorage.setItem("lang", lang);

  // оновити напис на кнопці
  const labelSpan = document.querySelector(".lang-btn span");
  if (labelSpan) labelSpan.textContent = dict.lang_label;
}

function _i18n_init(){
  const savedLang = localStorage.getItem("lang") || "uk";
  applyLanguage(savedLang);
}

if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', _i18n_init);
} else {
  // if script is loaded after DOMContentLoaded (scripts at end of body), apply immediately
  _i18n_init();
}
